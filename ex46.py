# Index from the end of sequences using negative numbers. The last element is at index -1.
r = [1, -4, 334, 43, -12]
print(r[-1])
print(r[-2])

# Slicing: Extended form of indexing for referring to a portion of a list or other sequence
# Syntax: a_list[start:stop]
print(r[1:-1])
print(r[1:])
print(r[:3])
print(r[:])
